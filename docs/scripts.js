            /** Fetch rating info for a player uuid. Returns null if no rating data. */
            function fetchRatingInfo(playerUuid) {
                if (!playerUuid || playerUuid === 'null') return null;
                const pr = query(`
                    SELECT pr.rating, pr.match_count,
                      (SELECT COUNT(*) FROM player_ratings WHERE rating > pr.rating) + 1 AS rank,
                      (SELECT COUNT(*) FROM player_ratings) AS total
                    FROM player_ratings pr WHERE pr.player_uuid = ?`, [playerUuid]);
                if (!pr.length) return null;
                const ko = query(`
                    SELECT COUNT(*) AS c FROM matches m
                    JOIN rounds r ON r.uuid = m.round_uuid
                    WHERE (m.player_a_uuid = ? OR m.player_b_uuid = ?)
                      AND r.name LIKE 'Top %'`, [playerUuid, playerUuid]);
                const rank = pr[0].rank, total = pr[0].total;
                return { rating: pr[0].rating, swiss: pr[0].match_count, ko: ko[0]?.c ?? 0, rank: rank / total <= 1/3 ? rank : null, total };
            }

            function getTierClass(rank, total) {
                if (!rank || !total) return '';
                const pct = rank / total;
                if (pct <= 0.02) return 'tier-elite';
                if (pct <= 0.05) return 'tier-sub-elite';
                if (pct <= 0.10) return 'tier-excellent';
                if (pct <= 0.25) return 'tier-great';
                return '';
            }

            function buildPlayerHeader(name, ratingInfo) {
                const tierClass = ratingInfo ? getTierClass(ratingInfo.rank, ratingInfo.total) : '';
                const ratingStr = ratingInfo
                    ? `<span style="font-weight:400;color:#64748b;font-size:0.85rem"> · Delo: ${Number(ratingInfo.rating).toFixed(2)}${ratingInfo.rank != null ? ` · ${ordinal(ratingInfo.rank)} of ${ratingInfo.total}` : ''} · ${ratingInfo.swiss} Swiss, ${ratingInfo.ko} KO</span>`
                    : '';
                return `<span class="player-name${tierClass ? ' ' + tierClass : ''}">${esc(name)}${ratingStr}</span>`;
            }

            /**
             * Build the Round/Opponent/Score/Result table for a player's matches in one competition.
             * Opponent names are rendered with a data-player-name attribute for post-render link wiring.
             */
            function renderMatchTable(matches, playerUuid) {
                if (!matches.length) return `<div class="empty" style="padding:0.5rem 0;text-align:left">No match records</div>`;
                const rows = matches.map(m => {
                    const isA = m.player_a_uuid === playerUuid;
                    const myScore = isA ? m.player_a_score : m.player_b_score;
                    const oppScore = isA ? m.player_b_score : m.player_a_score;
                    const oppName = isA ? m.player_b_name : m.player_a_name;
                    const oppUuid = isA ? m.player_b_uuid : m.player_a_uuid;
                    let result, cls;
                    if (m.winning_player_uuid === null) { result = 'Draw'; cls = 'draw'; }
                    else if (m.winning_player_uuid === playerUuid) { result = 'Win'; cls = 'win'; }
                    else { result = 'Loss'; cls = 'loss'; }
                    const oppLink = `<a href="#" data-player-name="${esc(oppName)}" class="player-nav-link">${esc(oppName)}</a>`;
                    return `<tr>
                        <td style="white-space:nowrap">${esc(m.round_name)}</td>
                        <td>${oppLink}</td>
                        <td style="text-align:center;white-space:nowrap">${myScore ?? '—'} – ${oppScore ?? '—'}</td>
                        <td><span class="badge ${cls}">${result}</span></td>
                    </tr>`;
                }).join('');
                return `<div class="nested-table-wrap"><table>
                    <thead><tr><th>Round</th><th>Opponent</th><th style="text-align:center">Score</th><th>Result</th></tr></thead>
                    <tbody>${rows}</tbody>
                </table></div>`;
            }

