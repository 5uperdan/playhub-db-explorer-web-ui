            // ═══════════════════════════════════════════════════════════
            //  Round sort helpers
            // ═══════════════════════════════════════════════════════════
            function roundSortKey(name) {
                const swiss = name.match(/^Round (\d+)$/);
                if (swiss) return [0, parseInt(swiss[1], 10)];

                const elim = name.match(/^Top (\d+)$/);
                // Negate so Top 8 (key -8) sorts before Top 4 (key -4) ascending
                if (elim) return [1, -parseInt(elim[1], 10)];

                return [2, 0];
            }

            function compareRounds(a, b) {
                const [at, an] = roundSortKey(a);
                const [bt, bn] = roundSortKey(b);
                return at !== bt ? at - bt : an - bn;
            }

