            let SQL = null;
            let db = null;

            initSqlJs({
                // Point at the CDN for the companion WASM binary
                locateFile: f => `https://cdnjs.cloudflare.com/ajax/libs/sql.js/1.10.2/${f}`
            }).then(sql => {
                SQL = sql;
            }).catch(() => {
                showError('Failed to load sql.js. Check your internet connection and refresh.');
            });

            // ═══════════════════════════════════════════════════════════
            //  Query helper — returns array of plain objects
            // ═══════════════════════════════════════════════════════════
            function query(sql, params = []) {
                const result = db.exec(sql, params);
                if (!result.length) return [];
                const { columns, values } = result[0];
                return values.map(row =>
                    Object.fromEntries(columns.map((col, i) => [col, row[i]]))
                );
            }

            // ═══════════════════════════════════════════════════════════
            //  Database loading
            // ═══════════════════════════════════════════════════════════
            function loadDatabase(file) {
                if (!file) return;
                if (!SQL) { showError('sql.js is still loading — please try again in a moment.'); return; }

                const reader = new FileReader();
                reader.onload = e => {
                    try {
                        const bytes = new Uint8Array(e.target.result);
                        if (db) db.close();
                        db = new SQL.Database(bytes);
                        // Sanity-check: must have a competitions table
                        const tables = query(
                            "SELECT name FROM sqlite_master WHERE type='table' AND name='competitions'"
                        );
                        if (!tables.length) throw new Error('Not a playhub.db file');
                        showApp();
                    } catch (err) {
                        showError('Could not open this file. Make sure it is a valid playhub.db generated by the CLI.');
                    }
                };
                reader.readAsArrayBuffer(file);
            }

