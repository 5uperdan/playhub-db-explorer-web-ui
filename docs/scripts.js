            // Build a CLI-style plain-text representation of a player's history
            function playerInfoText(playerUuid, name, ratingStr) {
                const header = ratingStr ? `${name} [${ratingStr.replace(/^ · /, '')}]` : name;
                const lines = [header];

                if (!playerUuid || playerUuid === 'null') {
                    lines.push('  (not found in database)');
                    lines.push('');
                    return lines.join('\n');
                }

                const competitions = query(`
                    SELECT c.uuid, c.name, c.start_date, c.attended_player_count, v.name AS venue_name, cr.position
                    FROM competition_results cr
                    JOIN competitions c ON c.uuid = cr.competition_uuid
                    LEFT JOIN venues v ON c.venue_uuid = v.ph_uuid
                    WHERE cr.player_uuid = ?
                    ORDER BY c.start_date ASC`, [playerUuid]);

                if (!competitions.length) {
                    lines.push('  (no competition results recorded)');
                    lines.push('');
                    return lines.join('\n');
                }

                for (const comp of competitions) {
                    const matches = query(`
                        SELECT m.player_a_uuid, m.player_b_uuid, m.player_a_score, m.player_b_score,
                               m.winning_player_uuid, r.name AS round_name,
                               pa.name AS player_a_name, pb.name AS player_b_name
                        FROM matches m
                        JOIN rounds r ON r.uuid = m.round_uuid
                        JOIN players pa ON pa.uuid = m.player_a_uuid
                        JOIN players pb ON pb.uuid = m.player_b_uuid
                        WHERE m.competition_uuid = ? AND (m.player_a_uuid = ? OR m.player_b_uuid = ?)`,
                        [comp.uuid, playerUuid, playerUuid]);

                    matches.sort((a, b) => compareRounds(a.round_name, b.round_name));

                    const playerCount = comp.attended_player_count ? ` (${comp.attended_player_count} players)` : '';
                    lines.push(`  ${comp.venue_name ?? 'Unknown Venue'}: ${comp.start_date}${playerCount}`);

                    for (const m of matches) {
                        const isA = m.player_a_uuid === playerUuid;
                        const paName = m.player_a_name;
                        const pbName = m.player_b_name;
                        const paScore = m.player_a_score;
                        const pbScore = m.player_b_score;
                        let matchLine;
                        if (m.winning_player_uuid === null) {
                            matchLine = `${paName} TIE ${pbName}`;
                        } else {
                            matchLine = `${paName} ${paScore} - ${pbScore} ${pbName}`;
                        }
                        lines.push(`    ${m.round_name}: ${matchLine}`);
                    }

                    if (comp.position != null) {
                        lines.push(`    Final position: ${comp.position}`);
                    }
                    lines.push('');
                }

                lines.push('');
                return lines.join('\n');
            }

